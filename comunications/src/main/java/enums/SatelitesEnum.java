package enums;

import dto.SateliteDto;

public enum SatelitesEnum {

	//KENOBI("Kenobi", -500D, -200D, 0D), SKYWALKER("Skywalker", 100D, -100D, 0D), SATO("Sato", 500D, 100D, 0D);

	KENOBI("Kenobi", 4D, 8D, 0D), SKYWALKER("Skywalker", 11D, 7D, 0D), SATO("Sato", 16D, 17D, 0D);
	
	private SateliteDto satelite;

	private SatelitesEnum(String name, double x, double y, double distancia) {
		this.satelite = new SateliteDto(name, "", x, y, distancia);
	}

	public SateliteDto getSatelite() {
		return this.satelite;
	}

}
